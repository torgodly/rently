شروط وأحكام تأجير السيارات
التسجيل والحسابات
1.1 يجب على المستأجرين تقديم معلومات صحيحة ودقيقة أثناء عملية التسجيل.
1.2 يتحمل المستأجرون المسؤولية الكاملة عن سرية معلومات حسابهم.
1.3 يجوز للمستأجرين تحديث معلومات الحساب بشكل دوري.

شروط الاستئجار
2.1 يجب أن يكون عمر المستأجر 21 عاماً على الأقل ويمتلك رخصة قيادة سارية المفعول منذ سنة على الأقل.
2.2 يجب تقديم بطاقة هوية سارية المفعول (بطاقة الهوية الوطنية أو جواز السفر) وبطاقة ائتمان باسم المستأجر.
2.3 يجب دفع مبلغ التأمين قبل استلام السيارة.

الرسوم والمدفوعات
3.1 جميع الرسوم والأسعار تشمل الضرائب المحلية.
3.2 يجب دفع أي رسوم إضافية، مثل رسوم الوقود أو التأخير في إعادة السيارة، عند إعادة السيارة.
3.3 يجب دفع المبلغ المستحق بالكامل عند استلام السيارة.

استخدام السيارة
4.1 يجب فحص السيارة قبل استلامها والإبلاغ عن أي أضرار موجودة مسبقاً.
4.2 يجب إعادة السيارة في نفس الحالة التي كانت عليها عند الاستلام، مع الأخذ في الاعتبار الاستهلاك العادي.
4.3 يمنع استخدام السيارة في أنشطة غير قانونية أو لجر أو سحب مركبات أخرى.

الصيانة والإصلاحات
5.1 في حال حدوث أي خلل في السيارة، يجب الاتصال بالشركة فوراً وعدم إجراء أي إصلاحات دون إذن مسبق.
5.2 يتحمل المستأجر تكاليف الإصلاحات الناتجة عن سوء الاستخدام أو الحوادث الناتجة عن الإهمال.
5.3 تشمل الصيانة العادية تغيير الزيت، وفحص الفرامل والإطارات.

الحوادث والتأمين
6.1 في حال وقوع حادث، يجب الاتصال بالشرطة وتقديم تقرير رسمي بالإضافة إلى إخطار الشركة.
6.2 يشمل التأمين أضرار الطرف الثالث وفقاً للشروط المحددة في عقد التأمين.
6.3 يتحمل المستأجر المسؤولية المالية عن الأضرار التي لا يغطيها التأمين.

الإلغاء
7.1 يمكن إلغاء الحجز قبل 24 ساعة من موعد الاستلام دون أي رسوم إضافية.
7.2 يتم خصم نسبة معينة من المبلغ المدفوع في حال الإلغاء المتأخر.
7.3 يجب على المستأجر الاتصال بالشركة لإجراءات الإلغاء.

إعادة السيارة
8.1 يجب إعادة السيارة في الوقت والمكان المحدد في العقد.
8.2 في حال التأخير، سيتم فرض رسوم إضافية وفقاً للسياسة المعتمدة.
8.3 يجب إعادة السيارة بكمية الوقود التي كانت موجودة عند الاستلام.

القوانين المعمول بها
9.1 تخضع هذه الشروط والأحكام للقوانين المحلية المعمول بها في بلد الاستئجار.
9.2 يجب على المستأجر الالتزام بجميع القوانين واللوائح المرورية.
9.3 في حالة النزاعات، تكون المحاكم المحلية هي المختصة.

الشروط الإضافية
10.1 يمكن تطبيق شروط إضافية وفقاً للعروض الخاصة أو السيارات الفاخرة.
10.2 يتم إعلام المستأجر بأي شروط إضافية عند الحجز.
10.3 يجب على المستأجر قراءة وفهم هذه الشروط قبل توقيع العقد.
